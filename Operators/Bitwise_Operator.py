# 5. BITWISE OPERATORS => USED TO PERFORM OPERATIONS ON BINARY NUMBERS.
# EX: &, |, ^, ~, <<, >>

x = 10
y = 4

print("The output of x & y : ", x & y)
print("The output of x | y : ", x | y)
print("The output of x ^ y : ", x ^ y)
print("The output of ~x : ", ~x)
print("The output of x << 1 : ", x << 1)
print("The output of x >> 1 : ", x >> 1)

