# 4. LOGICAL OPERATORS => USED TO COMBINE CONDITIONAL STATEMENTS.
# EX: and, or, not

a = True
b = False

print("The output of a and b : ", a and b)
print("The output of a or b : ", a or b)
print("The output of not a : ", not a)

x = int(input("Enter value of x: "))
y = int(input("Enter value of y: "))

print("x > 5 and y > 5 :", x > 5 and y > 5)
print("x > 5 or y > 5 :", x > 5 or y > 5)
print("not(x > 5) :", not(x > 5))